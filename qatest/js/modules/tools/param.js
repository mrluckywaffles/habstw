; 

(function(module){
	
	module.getParam = function ( sname ) {
		var params = location.search.substr(location.search.indexOf("?")+1);
		var sval = "";
		params = params.split("&");
		// split param and value into individual pieces
		for (var i=0; i<params.length; i++)
		{
			temp = params[i].split("=");
			if ( [temp[0]] == sname ) { sval = temp[1]; }
		}
		return sval;
	};

})(anime.module('tools'));
